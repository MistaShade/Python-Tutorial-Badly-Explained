x = 2.14
y = 3.56
z = -13.99

round = round(x)
# round function basically rounds a number!

absolute = abs(z)
# absolute value is the distance away from zero as a WHOLE number

power = pow(y, 4)
# This is a power function where it basically says y, power of 4 (you can raise a base to a given power!)

maximum = max(x, y, z)
# This is a maximum function (or max function) which finds the highest value!

minimum = min(x, y, z)
#This is a minimum function (or min function) which finds the lowest value!

print(round)
print(absolute)
print(power)
print(maximum)
print(minimum)