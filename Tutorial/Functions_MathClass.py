import math

x = 9
y = 2.56

print(math.pi)
print(math.e)
# (e means exponential constant)
result = math.sqrt(x)
result2 = math.ceil(y)
# ceil will always round a floating number up! (rounded up)
result3 = math.floor(y)
# while floor will do the opposite it goes back down! (rounded down)

print(f"{result}, {result2}, {result3}")